package pe.org.cnl.movil.persistence;

public class BeanNotaria {
	
	private int id ;
	private String de_comp;
	private String no_nota;
	private String de_dire;
	private String de_hora_nota;
	private String nu_tele_nota;
	private String de_mail_nota;
	private String de_url_nota;
	private String co_ubig;
	private int sisev;
	private int latitud;
	private int longitud;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDe_comp() {
		return de_comp;
	}
	public void setDe_comp(String de_comp) {
		this.de_comp = de_comp;
	}
	public String getNo_nota() {
		return no_nota;
	}
	public void setNo_nota(String no_nota) {
		this.no_nota = no_nota;
	}
	public String getDe_dire() {
		return de_dire;
	}
	public void setDe_dire(String de_dire) {
		this.de_dire = de_dire;
	}
	public String getDe_hora_nota() {
		return de_hora_nota;
	}
	public void setDe_hora_nota(String de_hora_nota) {
		this.de_hora_nota = de_hora_nota;
	}
	public String getNu_tele_nota() {
		return nu_tele_nota;
	}
	public void setNu_tele_nota(String nu_tele_nota) {
		this.nu_tele_nota = nu_tele_nota;
	}
	public String getDe_mail_nota() {
		return de_mail_nota;
	}
	public void setDe_mail_nota(String de_mail_nota) {
		this.de_mail_nota = de_mail_nota;
	}
	public String getDe_url_nota() {
		return de_url_nota;
	}
	public void setDe_url_nota(String de_url_nota) {
		this.de_url_nota = de_url_nota;
	}
	public String getCo_ubig() {
		return co_ubig;
	}
	public void setCo_ubig(String co_ubig) {
		this.co_ubig = co_ubig;
	}
	public int getSisev() {
		return sisev;
	}
	public void setSisev(int sisev) {
		this.sisev = sisev;
	}
	public int getLatitud() {
		return latitud;
	}
	public void setLatitud(int latitud) {
		this.latitud = latitud;
	}
	public int getLongitud() {
		return longitud;
	}
	public void setLongitud(int longitud) {
		this.longitud = longitud;
	}
	
}
